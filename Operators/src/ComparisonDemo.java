
public class ComparisonDemo {

	public static void main(String[] args) {
		int val1 = 1;
		int val2 = 2;
		
		if(val1 == val2) {
			System.out.println("val1 == val2");
		}
		if(val1 != val2) {
			System.out.println("val1 != val2");
		}
		if(val1 > val2) {
			System.out.println("var1 > var2");
		}
		if(val1 < val2) {
			System.out.println("var1 < var2");
		}
		if(val1 <= val2) {
			System.out.println("val1 <= val2");
		}
	}

}
